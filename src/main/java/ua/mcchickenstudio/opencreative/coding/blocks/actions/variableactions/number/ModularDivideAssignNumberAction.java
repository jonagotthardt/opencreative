package ua.mcchickenstudio.opencreative.coding.blocks.actions.variableactions.number;

import ua.mcchickenstudio.opencreative.coding.arguments.Arguments;
import ua.mcchickenstudio.opencreative.coding.blocks.actions.ActionType;
import ua.mcchickenstudio.opencreative.coding.blocks.actions.Target;
import ua.mcchickenstudio.opencreative.coding.blocks.actions.variableactions.VariableAction;
import ua.mcchickenstudio.opencreative.coding.blocks.executors.Executor;
import ua.mcchickenstudio.opencreative.coding.variables.VariableLink;
import org.bukkit.entity.Entity;

public class ModularDivideAssignNumberAction extends VariableAction {
    public ModularDivideAssignNumberAction(Executor executor, Target target, int x, Arguments args) {
        super(executor, target, x, args);
    }

    @Override
    protected void execute(Entity entity) {
        VariableLink variable = getArguments().getVariableLink("variable",this);
        double variableValue = getArguments().getValue("variable",0.0d,this);
        double number = getArguments().getValue("number",1.0d,this);
        double result = variableValue % number;
        setVarValue(variable, result);
    }

    @Override
    public ActionType getActionType() {
        return ActionType.VAR_MODULAR_DIVIDE_ASSIGN_NUMBER;
    }
}
