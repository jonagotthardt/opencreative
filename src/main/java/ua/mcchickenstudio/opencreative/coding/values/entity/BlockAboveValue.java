package ua.mcchickenstudio.opencreative.coding.values.entity;

import org.bukkit.Location;
import org.bukkit.Material;
import org.bukkit.block.Block;
import org.bukkit.block.BlockFace;
import org.bukkit.entity.Entity;
import org.bukkit.inventory.ItemStack;
import org.jetbrains.annotations.NotNull;
import org.jetbrains.annotations.Nullable;
import ua.mcchickenstudio.opencreative.coding.blocks.actions.Action;
import ua.mcchickenstudio.opencreative.coding.blocks.actions.ActionsHandler;
import ua.mcchickenstudio.opencreative.coding.menus.MenusCategory;
import ua.mcchickenstudio.opencreative.coding.values.LocationEventValue;

public final class BlockAboveValue extends LocationEventValue {

    public BlockAboveValue() {
        super("block_above", new ItemStack(Material.CHERRY_TRAPDOOR), MenusCategory.ENTITY);
    }

    @Override
    public @Nullable Location getLocation(@NotNull ActionsHandler handler, @NotNull Action action, @Nullable Entity entity) {
        if (entity == null) return null;
        Block block = entity.getLocation().getBlock().getRelative(BlockFace.UP);
        if (block.getType().isAir()) {
            block = block.getRelative(BlockFace.UP);
        }
        return block.getLocation();
    }

    @Override
    public @NotNull String getExtensionId() {
        return "default";
    }

    @Override
    public @NotNull String getDescription() {
        return "Returns block, that is above the entity";
    }
}
