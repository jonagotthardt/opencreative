package mcchickenstudio.creative.debug.values;

import mcchickenstudio.creative.coding.blocks.events.EventValues;
import mcchickenstudio.creative.coding.blocks.executors.Executor;
import mcchickenstudio.creative.coding.variables.ValueType;

public record EventValueLink(EventValues.Variable type, Executor executor) implements CodingValue {

    @Override
    public String toString() {
        return "Event Value: " + type.getLocaleName();
    }

    @Override
    public ValueType getType() {
        return ValueType.EVENT_VALUE;
    }

    @Override
    public void setValue(Object value) {}

    @Override
    public Object getValue(boolean deep) {
        return this;
    }

    @Override
    public Object serialize() {
        return null;
    }
}
